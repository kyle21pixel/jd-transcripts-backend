<script>
document.addEventListener('DOMContentLoaded', function() {
    const form = document.getElementById('career-form');
    const fileUpload = document.getElementById('resume-upload');
    const fileInput = document.getElementById('resume');
    const fileList = document.getElementById('resume-list');
    const statusDiv = document.getElementById('application-status');
    
    // File upload drag and drop
    if (fileUpload && fileInput) {
        fileUpload.addEventListener('dragover', function(e) {
            e.preventDefault();
            fileUpload.classList.add('dragover');
        });
        
        fileUpload.addEventListener('dragleave', function(e) {
            e.preventDefault();
            fileUpload.classList.remove('dragover');
        });
        
        fileUpload.addEventListener('drop', function(e) {
            e.preventDefault();
            fileUpload.classList.remove('dragover');
            
            const files = e.dataTransfer.files;
            if (files.length > 0) {
                fileInput.files = files;
                displayFiles(files);
            }
        });
        
        fileInput.addEventListener('change', function() {
            displayFiles(this.files);
        });
    }
    
    function displayFiles(files) {
        fileList.innerHTML = '';
        
        for (let i = 0; i < files.length; i++) {
            const file = files[i];
            const fileItem = document.createElement('div');
            fileItem.className = 'file-item';
            
            const fileName = document.createElement('span');
            fileName.className = 'file-name';
            fileName.textContent = file.name;
            
            const fileSize = document.createElement('span');
            fileSize.className = 'file-size';
            fileSize.textContent = formatFileSize(file.size);
            
            fileItem.appendChild(fileName);
            fileItem.appendChild(fileSize);
            fileList.appendChild(fileItem);
        }
    }
    
    function formatFileSize(bytes) {
        if (bytes === 0) return '0 Bytes';
        const k = 1024;
        const sizes = ['Bytes', 'KB', 'MB', 'GB'];
        const i = Math.floor(Math.log(bytes) / Math.log(k));
        return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
    }
    
    // Form submission
    if (form) {
        form.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const submitBtn = form.querySelector('button[type="submit"]');
            const originalText = submitBtn.textContent;
            
            // Show loading state
            submitBtn.textContent = 'Submitting Application...';
            submitBtn.disabled = true;
            
            statusDiv.className = 'form-status loading';
            statusDiv.textContent = 'Processing your application...';
            
            // Create FormData object
            const formData = new FormData(form);
            
            // Submit form via AJAX
            fetch(form.action, {
                method: 'POST',
                body: formData
            })
            .then(response => {
                if (response.ok) {
                    statusDiv.className = 'form-status success';
                    statusDiv.textContent = 'Application submitted successfully! We will contact you within 2-3 business days.';
                    
                    submitBtn.textContent = 'Application Submitted';
                    
                    // Reset form after success
                    setTimeout(() => {
                        form.reset();
                        fileList.innerHTML = '';
                        submitBtn.textContent = originalText;
                        submitBtn.disabled = false;
                        statusDiv.textContent = '';
                        statusDiv.className = 'form-status';
                    }, 5000);
                } else {
                    throw new Error('Network response was not ok');
                }
            })
            .catch(error => {
                console.error('Error:', error);
                statusDiv.className = 'form-status error';
                statusDiv.textContent = 'There was an error submitting your application. Please try again.';
                
                submitBtn.textContent = originalText;
                submitBtn.disabled = false;
            });
        });
    }

    // Smooth scrolling for navigation
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
});
