<script>
document.addEventListener('DOMContentLoaded', function() {
    const applicationForm = document.getElementById('application-form');
    
    if (applicationForm) {
        applicationForm.addEventListener('submit', function(e) {
            e.preventDefault();
            
            // Get form data
            const formData = new FormData(applicationForm);
            const applicationData = {
                name: formData.get('applicant-name'),
                email: formData.get('applicant-email'),
                phone: formData.get('applicant-phone'),
                position: formData.get('position-applied'),
                experience: formData.get('experience-years'),
                coverLetter: formData.get('cover-letter'),
                availability: formData.get('availability'),
                timestamp: new Date().toISOString()
            };
            
            // Show success message
            const statusDiv = document.getElementById('application-status');
            if (statusDiv) {
                statusDiv.innerHTML = '<div style="color: green; padding: 15px; background: rgba(0,255,0,0.1); border-radius: 8px; margin: 15px 0;">✅ Application submitted successfully! We\'ll get back to you within 48 hours.</div>';
                statusDiv.style.display = 'block';
            }
            
            // Reset form
            applicationForm.reset();
            
            // Hide status after 5 seconds
            setTimeout(() => {
                if (statusDiv) statusDiv.style.display = 'none';
            }, 5000);
        });
    }
});
</script>

